function f = func7(t, x, m1, m2, G)
    f = zeros(8,1);
    f(1) = x(2);
    f(2) = G * m2 * (x(5)-x(1))./(norm([x(1),x(3)]-[x(5),x(7)])).^3;
    f(3) = x(4);
    f(4) = G * m2 * (x(7)-x(3))./(norm([x(1),x(3)]-[x(5),x(7)])).^3;
    f(5) = x(6);
    f(6) = G * m1 * (x(1)-x(5))./(norm([x(1),x(3)]-[x(5),x(7)])).^3;
    f(7) = x(8);
    f(8) = G * m1 * (x(3)-x(7))./(norm([x(1),x(3)]-[x(5),x(7)])).^3;
end